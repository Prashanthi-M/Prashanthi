package objectsWithConditions;

public class SwCap {
	public static void main(String[] args) {
		String capital="Kerala";
		switch(capital) {
		case "Tamilnadu" : System.out.println("Chennai");
		break;
		case "Kerala" : System.out.println("Kochin");
		break;
		case "Karnataka" : System.out.println("Bangalore");
		break;
		case "Maharashtra" : System.out.println("Mumbai");
		break;
		case "Andhra" : System.out.println("Hyderabad");
		break;
		case "Telungana" : System.out.println("Vishakapattinam");
		break;
		case "WestBengal" : System.out.println("Kolkata");
		break;
		default: System.out.println("None");
		
			
			
		}
	}
	}


